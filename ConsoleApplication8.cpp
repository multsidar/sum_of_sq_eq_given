#include <iostream>
#include <vector>
#include <algorithm>
#include <cmath>
using namespace std;
int main()
{
    long long num;
    vector<long long> v;
    cin >> num;
    for (long long i = 0; i < sqrt(num); i++)
    {
        long long k = i * i;
        v.push_back(k);
    }
    for (long long i = 0; i < sqrt(num); i++)
    {
        if (binary_search(v.begin(), v.end(), num - (i * i)))
        {
            cout << i << "^2+" << sqrt(num - i * i) << "^2" << endl;
        }
    }
}

